from time import time
from turtle import *
color('black')
begin_fill()
penup()
while True:
    setx(-170)
    sety(195)
    pendown()
    speed(9)
    setheading(90)
    circle(75,270)
    setheading(180)
    circle(75, -270)
    penup()
    setx(-100)
    sety(270)
    pendown()
    forward(300)
    penup()
    setx(20)
    sety(270)
    pendown()
    forward(250)
    setheading(90)
    circle(50)
    forward(-50)
    penup()
    setx(100)
    sety(270)
    pendown()
    forward(-300)
    penup()
    sety(0)
    pendown()
    setheading(270)
    circle(50, -180)
    forward(-50)
    penup()
end_fill()
done()